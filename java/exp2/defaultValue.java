import java.util.*;
class defaultValue{
    static int i;
    static float f;
    static double d;
    static String s;
    static boolean bool;
    static byte b;
    public static void main(String args[]){
        System.out.println("the default value of an integer is " + i );
        System.out.println("the default value of a float is " + f );
        System.out.println("the default value of a double is " + d );
        System.out.println("the default value of a string is " + s );
        System.out.println("the default value of a boolean is " + bool);
        System.out.println("the default value of a byte is " + b);
        
    }
}